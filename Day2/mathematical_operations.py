# Order of operations (PEMDAS) Parentheses, Exponents, Multiplication, Division, Addition, Subtraction.
# Brackets, Exponents (**), multiplication, division, addition, subtraction.
# Multiplication and division are the same priority, as well as addition and subtraction.

print(3 * 3 + 3 / 3 - 3)
# 3 x 3 = 9, then 3 / 3 = 1, then adding those 2 and minus 3 = 7
print (3 * 3 / 3 + 3)
# 3 x 3 = 9, 9 / 3 = 3, 3 + 3
